		public static void Main() {
			uint value, val;
			string input, output;
			Console.WriteLine("Append with 'h' for hex (ex. 400h).\n");
			do {
				Console.Write("Window Message: ");
				input = Console.ReadLine().Trim();
				if (input.Length >= 2 && char.ToLower(input[input.Length - 1]) == 'h')
					uint.TryParse(input.Substring(0, input.Length - 1), NumberStyles.HexNumber, CultureInfo.CurrentCulture, out value);
				else
					uint.TryParse(input, out value);
				output = ((WindowMessage) value).ToString();
				if (uint.TryParse(output, out val))
					Console.WriteLine("Not found.");
				else
					Console.WriteLine(output + " = " + value.ToString());
				Console.WriteLine();
			} while (true);
		}

